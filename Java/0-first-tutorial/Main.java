// The class name has to be the same as the file name
public class Main {
    // The function name, main, is preserved
    // main function is the entry point of the program
    // public: access by public (anyone, anywhere: JVM)
    // static: making itself a class method so to be called without creating an obj of the class?
    // void: return type of the function, void returns nothing
    public static void main(String[] args) {
        System.out.println("Hello world!");
    }
}