package com.kai;
// Primitive data means data cannot be further broken into smaller piece
public class Primitives {
    public static void main(String[] args) {
        int rollNum = 64;
        // char is enclosed with single quotes '
        char letter = 'S';
        float marks = 92.3f;
        double largeDecimalNumbers = 324870987380478204.3232;
        long largeInteger = 2304723047047928372L;
        boolean check = true;
        // String is not primitive - data
        String name = "Louis";
    }
}
